public class Aufgabe5_1810653996 {

    public static void main(String[] args) {

        int zahl1 = 40;
        int zahl2 = 25;


        System.out.println(zahl1++);
        System.out.println(zahl2++);
        System.out.println(zahl1);
        System.out.println(zahl2);
        System.out.println(++zahl1);
        System.out.println(++zahl2);

        /* Der Unterschied zwischen a++ und ++a besteht darin, dass bei a++ nach dem ausgeben der Variable die Variable
         erhöht wird. Im Gegensatz dazu wird bei ++a die Variable vor dem Ausgeben erhöht.*/







    }

}
